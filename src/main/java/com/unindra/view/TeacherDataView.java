    private void teacherTableMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_teacherTableMouseClicked
        
        int row = teacherTable.getSelectedRow();
        Object birthDate = teacherTable.getModel().getValueAt(row, 3);

        nameForm.setText(teacherTable.getModel().getValueAt(row, 1).toString());
        idForm.setText(teacherTable.getModel().getValueAt(row, 0).toString());
        birthPlaceForm.setText(teacherTable.getModel().getValueAt(row, 2).toString());
        // birthDateForm.setDate((Date)birthDate);
        genderForm.setSelectedItem(teacherTable.getModel().getValueAt(row, 4).toString());
        addressForm.setText(teacherTable.getModel().getValueAt(row, 5).toString());
        phoneNumberForm.setText(teacherTable.getModel().getValueAt(row, 6).toString());
        emailForm.setText(teacherTable.getModel().getValueAt(row, 7).toString());

        LocalDate localDate = (LocalDate) birthDate;
        Instant instant = localDate.atStartOfDay(ZoneId.systemDefault()).toInstant();
        Date date = Date.from(instant);

        birthDateForm.setDate(date);

    }// GEN-LAST:event_teacherTableMouseClicked

    private void editButtonMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_editButtonMouseClicked
        
        String id = idForm.getText();

        TeacherRequest teacher = new TeacherRequest();
        teacher.setName(nameForm.getText());
        teacher.setId(idForm.getText());
        teacher.setBirthPlace(birthPlaceForm.getText());
        teacher.setAddress(addressForm.getText());
        teacher.setGender(genderForm.getSelectedItem().toString());
        teacher.setEmail(emailForm.getText());
        teacher.setPhoneNumber(phoneNumberForm.getText());

        Date date = birthDateForm.getDate();
        teacher.setBirthDate(teacherService.toLocalDate(date));

        try {
            teacherService.update(id, teacher);
            JOptionPane.showMessageDialog(this, "Update data berhasil");
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Validasi Gagal", JOptionPane.ERROR_MESSAGE);
        }
        loadData();
        clearForm();

    }// GEN-LAST:event_editButtonMouseClicked

    private void clearButtonMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_clearButtonMouseClicked
        
        clearForm();
    }// GEN-LAST:event_clearButtonMouseClicked

    private void deleteButtonMouseClicked(java.awt.event.MouseEvent evt) {// GEN-FIRST:event_deleteButtonMouseClicked

        if (idForm.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pilih data untuk dihapus", "Validasi Gagal", JOptionPane.ERROR_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(
                    this,
                    "Apakah Anda yakin ingin menghapus data ini?",
                    "Konfirmasi Hapus",
                    JOptionPane.YES_NO_OPTION,
                    JOptionPane.WARNING_MESSAGE);

            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    teacherService.delete(idForm.getText());
                    JOptionPane.showMessageDialog(this, "Berhasil menghapus data");
                    loadData();
                    clearForm();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "Validasi Gagal", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }

