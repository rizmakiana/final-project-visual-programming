    private void addDataButtonActionPerformed(java.awt.event.ActionEvent evt) {

        TeacherRequest teacher = new TeacherRequest();

        teacher.setName(nameForm.getText());
        teacher.setId(idForm.getText());
        teacher.setBirthPlace(birthPlaceForm.getText());
        teacher.setAddress(addressForm.getText());
        teacher.setGender(genderForm.getSelectedItem().toString());
        teacher.setEmail(emailForm.getText());
        teacher.setPhoneNumber(phoneNumberForm.getText());

        // teacher.setBirthDate(birthDate)
        Date date = birthDateForm.getDate();
        teacher.setBirthDate(teacherService.toLocalDate(date));

        try {
            teacherService.addData(teacher);
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
            clearForm();
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Validasi Gagal", JOptionPane.ERROR_MESSAGE);
        }
        loadData();

    }

    public void loadData() {
        Object header[] = { "Name", "NIK", "Tempat Lahir", "Tanggal lahir", "Gender", "Alamat", "No. Telpon", "Email" };

        DefaultTableModel data = new DefaultTableModel(null, header);

        TeacherRequest[] allData = teacherService.getAllData();

        teacherTable.setModel(data);

        for (TeacherRequest teacherRequest : allData) {
            data.addRow(new Object[] {
                    teacherRequest.getName(),
                    teacherRequest.getId(),
                    teacherRequest.getBirthPlace(),
                    teacherRequest.getBirthDate(),
                    teacherRequest.getGender(),
                    teacherRequest.getAddress(),
                    teacherRequest.getPhoneNumber(),
                    teacherRequest.getEmail()
            });
        }
        log.info("succesfully load data ");
    }


